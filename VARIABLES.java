public class VARIABLES {
    public static void main(String[] args) {
        var A = 100;                int a =100;
        var B = 'c';                char b ='c';
        var C = true;               boolean c =false;
        var D = 10.002;             float d = 10.002f;
        var E = 10.000333210;       double e = 10.000333210;
        System.out.println("A = " + A + ", B = " + B + ", C = " + C + ", D = " + D);
        System.out.println(A+" "+B+" "+C+" "+D);
        System.out.print(a);   System.out.println(A);
        System.out.print(b);   System.out.println(B);
        System.out.print(c);   System.out.println(C);
        System.out.print(d);   System.out.println(D);
        System.out.print(e);   System.out.println(E);

    }
}

/*  Primitive Types

int: Stores integers (whole numbers).
Example: int age = 25;

double: Stores floating-point numbers (decimals).
Example: double price = 19.99;

float: Stores floating-point numbers (decimals) with less precision.
Example: float pi = 3.14f;

char: Stores a single character.
Example: char grade = 'A';

boolean: Stores true or false.
Example: boolean isActive = true;

byte: Stores small integers (-128 to 127).
Example: byte b = 100;

short: Stores small integers (-32,768 to 32,767).
Example: short s = 1000;

long: Stores large integers.
Example: long distance = 123456789L;  */

