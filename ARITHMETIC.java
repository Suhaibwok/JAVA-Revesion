public class ARITHMETIC {
    public static void main(String[] args) {
        int A = 10;
        int B = 10;
        char C = 'A';
        float D = 110.123f;
        String E ="true";
        System.out.println(A+B);
        System.out.println(A-D);
        System.out.println(B+C);
        System.out.println(A/D);
        System.out.println(B%C);
        System.out.println(E+A);
    }
}
