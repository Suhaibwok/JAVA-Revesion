public class ARITHMETIC {
    public static void main(String[] args) {
        int A = 10;
        int B = 10;
        char C = 'A';
        float D = 110.123f;
        String E ="true";
        System.out.println(A+B);
        System.out.println(A-D);
        System.out.println(B+C);
        System.out.println(A/D);
        System.out.println(B%C);
        System.out.println(E+A);
    }
}
/*
List of Arithmetic Operators in Java:
Addition (+): Adds two operands.

Example: int sum = 5 + 3; → sum = 8
Subtraction (-): Subtracts the second operand from the first.

Example: int difference = 5 - 3; → difference = 2
Multiplication (*): Multiplies two operands.

Example: int product = 5 * 3; → product = 15
Division (/): Divides the first operand by the second.

Example: int quotient = 6 / 3; → quotient = 2
Note: If both operands are integers, the result is also an integer (fractional part is discarded). To get a decimal result, at least one operand should be a floating-point type (float or double).
Modulus (%): Returns the remainder when the first operand is divided by the second.

Example: int remainder = 5 % 3; → remainder = 2
 */
